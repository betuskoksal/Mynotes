/* 
-Confirm fonksiyonu, JavaScript'te bir tarayıcı penceresinde kullanıcıya bir onay iletişim 
kutusu (confirmation dialog) göstermek için kullanılır. Bu iletişim kutusu, 
genellikle bir işlemi gerçekleştirmek isteyip istemediğini doğrulamak veya reddetmek 
amacıyla kullanılır.

-Confirm fonksiyonu bir boolean değer döndürür. Eğer kullanıcı "OK" (Tamam) düğmesine tıklarsa,
 fonksiyon true değerini döner. Eğer "Cancel" (İptal) düğmesine tıklarsa, fonksiyon false 
 değerini döner.

*/